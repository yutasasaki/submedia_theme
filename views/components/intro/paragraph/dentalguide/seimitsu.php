<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading">①精密検査も含めて初診料が安い</h3>
<!-- /wp:heading -->

<!-- wp:image {"id":870,"sizeSlug":"large","linkDestination":"none"} -->
<figure class="wp-block-image size-large"><img src="<?php echo esc_url(get_stylesheet_directory_uri() . '/images/intro/dentalguide_intro/shoshin_yasui-1024x662.webp'); ?>" alt="初診料が安い" class="wp-image-870" /></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>歯科矯正の初診料は一般的に<strong>2万円〜5万円掛かる</strong>と言われています。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p><span class="marker-yellow">「カウンセリング無料」と広告をしている矯正歯科でも、実は精密検査代として別途5万円を取るところもあります。。</span></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>ですがキレイライン矯正の提携クリニックは精密検査を含めて<strong class="text-red">初診料が3,500円です！</strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>この価格で他の矯正歯科同様の精密検査が受けれて相談も出来るので、キレイライン矯正の提携クリニックはオススメです。</p>
<!-- /wp:paragraph -->


