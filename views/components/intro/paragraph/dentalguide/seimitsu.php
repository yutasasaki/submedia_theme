<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading">②キレイライン矯正が治療ができない場合、適した治療法を提案してくれる</h3>
<!-- /wp:heading -->

<!-- wp:image {"id":871,"sizeSlug":"large","linkDestination":"none"} -->
<figure class="wp-block-image size-large"><img src="<?php echo esc_url(get_stylesheet_directory_uri() . '/images/intro/dentalguide_intro/etc-1024x662.webp'); ?>" alt="他の治療" class="wp-image-871" /></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>低価格マウスピース矯正を代表するキレイライン矯正ですが、キレイライン矯正だけを扱っている矯正歯科はないため、キレイライン矯正で治療ができない場合、<span class="marker-yellow">どんな矯正治療なら出来るのか</span>提案をしてもらえます。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>そのため<span class="marker-yellow"><strong class="text-red">先生が信頼できて治療計画に納得が出来れば</strong>、わざわざ複数の矯正歯科を回る必要がなくなります！</span></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>もちろん複数の矯正歯科をまわって比較をしたり家でじっくり考えてから決めることも出来るので、まずは全国に150ヶ所以上あるキレイライン矯正の提携クリニックに行ってみて下さい。</p>
<!-- /wp:paragraph -->