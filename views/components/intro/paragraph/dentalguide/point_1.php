<h2 class=""><strong>歯科矯正を考え始めたら<br>はじめに確認したほうがよいこと</strong></h2>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h3 class="">矯正歯科を選ぶときの<wbr>大事なポイント</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>今まで<strong class="text-red">高い治療費がかかったのに歯並びがシミュレーション通りにならない</strong>方を沢山見てきました。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>自分に合った矯正歯科を探すときに<strong class="text-red">忘れてはいけない大事なこと</strong>がありますのでしっかりと確認をして、自分に合った矯正歯科を見つけて下さい。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading">2つ以上のマウスピース矯正の扱いがある矯正歯科</h3>
<!-- /wp:heading -->

<!-- wp:image -->
<figure class="wp-block-image size-large"><img src="<?php echo esc_url(get_stylesheet_directory_uri() . '/images/intro/dentalguide_intro/mouthpiece-1024x639.webp'); ?>" alt="インビザラインやキレイライン" class="wp-image-110" />
  <figcaption class="wp-element-caption">インビザラインやキレイライン</figcaption>
</figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>実はマウスピース矯正には沢山の種類が存在しています。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>大きくは2つに分けられていて「奥歯まで対応出来る<span class="marker-yellow"><strong>普通のマウスピース矯正</strong></span>」と「前歯のみ対応の<span class="marker-yellow"><strong class="text-red">低価格マウスピース矯正</strong></span>」があります。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>奥歯まで対応出来る普通のマウスピース矯正は<span class="marker-yellow">70万円〜120万円</span>程度で、前歯のみ対応の低価格マウスピース矯正は<span class="marker-yellow">20万円〜40万円</span>程度が相場の価格です。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>ポイントは下の表にまとめましたので確認してみて下さい。</p>
<!-- /wp:paragraph -->

<!-- wp:image {"id":869,"sizeSlug":"large","linkDestination":"none"} -->
<figure class="wp-block-image size-large"><img src="<?php echo esc_url(get_stylesheet_directory_uri() . '/images/intro/dentalguide_intro/compare_table.webp'); ?>" alt="2つのマウスピース矯正の比較" class="wp-image-869" /></figure>
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>こう見ると他人から見える範囲の矯正で十分な人は<span class="marker-yellow"><strong class="text-red">低価格マウスピース矯正</strong></span>で治療をしたいですよね。しかし多くの矯正歯科ではまず<strong><span class="marker-yellow">普通のマウスピース矯正</span></strong>を勧められます。これには理由が2つあります。</p>
<figure class="wp-block-image size-large"><img src="<?php echo esc_url(get_stylesheet_directory_uri() . '/images/intro/dentalguide_intro/yasui_banner.webp'); ?>" alt="安い=治療の質が悪いは間違いです！" class="wp-image-869" /></figure>
<!-- /wp:paragraph -->

