</figure>
<!-- /wp:image -->


<!-- wp:paragraph -->
<p>これを聞くと、<br>
  <strong>「低価格なものは、治療の質が悪いんじゃ…？」</strong>と不安に思う方もいるかもしれませんが、それは間違いです！
</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="font-bold">
  <span class="marker-yellow">値段が違う理由は、<strong>
      <span class="color-red">"治療範囲が違うから"</span>
    </strong>であり、治療の質とは関係ありません。</span>
</p>
<figure class="wp-block-image size-large">
  <img src="<?php echo esc_url(get_stylesheet_directory_uri() . '/images/intro/kireilign_v4/2_different.webp'); ?>" alt="安い＝治療の質が悪い
