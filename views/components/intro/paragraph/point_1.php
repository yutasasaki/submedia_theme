</figure> -->
<!-- /wp:image -->

<!-- wp:paragraph -->
<p>ただ残念ながら、<span class="marker-yellow font-bold">ほとんどの矯正歯科では普通のマウスピース矯正（インビザライン）しか扱っていません。</span>
  <br>その方が歯医者にとっての利益が大きいからです。
</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>そのため歯並びのズレが<strong>軽度〜中度</strong>だとしても、普通のマウスピース矯正をすることになります。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>
  <span class="marker-yellow">
    <strong>本来ならもっと安くできるのに、70〜120万円も払うことになる</strong>のは非常にもったいないですよね…。</span>
</p>
<!-- /wp:paragraph -->

<!-- wp:image -->
<figure class="wp-block-image size-large">
  <img src="<?php echo esc_url(get_stylesheet_directory_uri() . '/images/intro/kireilign_v4/3_zure.webp'); ?>" alt="もし軽度～中度の
ズレの場合...
