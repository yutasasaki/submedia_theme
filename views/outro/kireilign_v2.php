<!-- wp:paragraph -->
<p style="text-align: center;"><strong>「いつか矯正したいけど、</strong><br><strong>歯医者に行くのはまた後でいいや…」</strong><br>という方へ。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p style="text-align: center;">もしあなたが、少しでも歯並びを治したいと思っているなら<br>ぜひ"今"、初診の予約を取ることをオススメします。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p style="text-align: center;">矯正歯科治療では、費用や期間など<br>歯科医師にしか分からないことも多いです。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p style="text-align: center;">たしかに初診に行くのは少し面倒だし、<br>どの矯正歯科に行けばいいか迷うかもしれません。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p style="text-align: center;">でも、今少しだけ勇気を出して<br>"予約をする"という小さな一歩を踏み出せたら、</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p style="text-align: center;">将来の自分が必ず<br><strong><span class="marker-yellow">「あの</span><span class="marker-yellow">とき</span><span class="marker-yellow">矯正を始めてよかった！」</span></strong><br>と喜んでくれるはずです。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p style="text-align: center;"><strong>想像してみてください。</strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p style="text-align: center;">矯正が終わった日、鏡に映る自分を見て<br>「歯並びキレイになった！」<br>「矯正してよかった！」<br>と思えてたら、嬉しいと思いませんか？</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p style="text-align: center;"><strong><span class="marker-yellow">少しでも矯正しようと思った"今"が<br>自分を変える最大のチャンスです！</span></strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p style="text-align: center;">ぜひ一度、初診に行ってみてください。</p>
